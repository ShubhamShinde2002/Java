// public int indexOf(char ch,int fromIndex)
// FINDS THE FIRST INSTANCE OF THE CHARACTER IN THE GIVEN STRING
//
// PARAMETER - character(ch to find),Integer(Index to starts the search)
// RETURN TYPE - Integer


class indexOfDemo{
	public static void main(String[] args){
		String str1="Shashi";
		System.out.println(str1.indexOf('h',0));
		System.out.println(str1.indexOf('h',1));
		System.out.println(str1.indexOf('h',2));
	}
}
